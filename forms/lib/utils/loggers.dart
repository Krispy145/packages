/// [FormsLoggers] class responsible for storing base feature references.
/// Used to determine which features are enabled for logging.
enum FormsLoggers {
  /// [forms] is the feature reference for forms.
  forms,

  /// [field] is the feature reference for field.
  field,

  /// [maps] is the feature reference for maps.
  maps,

  /// [models] is the feature reference for models.
  models,
}
